package fr.hoenheimsports.trainingservice.model;

/**
 * Represents the gender of a person in the system.
 * 
 * <p>This enum provides the standard gender options used throughout the application.</p>
 * 
 * @since 1.0
 */
public enum Gender {
    /**
     * Represents female gender.
     */
    F, 

    /**
     * Represents male gender.
     */
    M, 

    /**
     * Represents neutral or non-binary gender.
     */
    N
}
